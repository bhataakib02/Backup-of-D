# Simple report generation function
def generate_simple_report(report_type, format_type, filters=None):
    """Generate a simple report"""
    report_id = f"RPT_{hashlib.md5(f'{time.time()}'.encode()).hexdigest()[:12].upper()}"
    
    # Generate report data
    report_data = {
        'report_id': report_id,
        'title': f'{report_type.replace("_", " ").title()} Report',
        'type': report_type,
        'format': format_type,
        'generated_at': datetime.now().isoformat(),
        'version': '2.1.0',
        'confidentiality_level': 'Internal',
        'metrics': {
            'total_threats': random.randint(1000, 5000),
            'critical_threats': random.randint(50, 200),
            'high_threats': random.randint(200, 500),
            'medium_threats': random.randint(300, 800),
            'low_threats': random.randint(200, 1000),
            'detection_accuracy': random.uniform(0.85, 0.98),
            'response_time': random.uniform(5, 30),
            'compliance_score': random.uniform(0.80, 0.95)
        },
        'recommendations': [
            'Implement zero-trust architecture',
            'Enhance AI/ML threat detection',
            'Update security awareness training',
            'Strengthen compliance controls'
        ],
        'executive_summary': {
            'overview': f'This {report_type.replace("_", " ")} report provides comprehensive analysis of security posture and compliance status.',
            'key_findings': [
                'Security posture is generally strong',
                'Some compliance gaps identified',
                'Recommendations for improvement provided'
            ],
            'risk_level': random.choice(['Low', 'Medium', 'High']),
            'next_steps': [
                'Review findings with stakeholders',
                'Develop remediation plan',
                'Schedule follow-up assessment'
            ]
        }
    }
    
    # Generate content based on format
    if format_type == 'pdf':
        # Simple PDF content (base64 encoded)
        pdf_content = f"""
        {report_data['title']}
        {'=' * len(report_data['title'])}
        
        Generated: {report_data['generated_at']}
        Report ID: {report_data['report_id']}
        
        EXECUTIVE SUMMARY
        {report_data['executive_summary']['overview']}
        
        KEY METRICS
        Total Threats: {report_data['metrics']['total_threats']}
        Critical Threats: {report_data['metrics']['critical_threats']}
        Detection Accuracy: {report_data['metrics']['detection_accuracy']:.1%}
        Response Time: {report_data['metrics']['response_time']:.1f} minutes
        
        RECOMMENDATIONS
        {chr(10).join(f"• {rec}" for rec in report_data['recommendations'])}
        
        Generated by NEXUS CYBER INTELLIGENCE Platform
        """
        content = base64.b64encode(pdf_content.encode()).decode()
        size = len(pdf_content.encode())
        
    elif format_type == 'html':
        html_content = f"""
        <!DOCTYPE html>
        <html>
        <head>
            <title>{report_data['title']}</title>
            <style>
                body {{ font-family: Arial, sans-serif; margin: 40px; }}
                h1 {{ color: #1e3a8a; }}
                .metric {{ background: #f1f5f9; padding: 10px; margin: 10px 0; border-radius: 5px; }}
                .recommendation {{ margin: 5px 0; }}
            </style>
        </head>
        <body>
            <h1>{report_data['title']}</h1>
            <p><strong>Generated:</strong> {report_data['generated_at']}</p>
            <p><strong>Report ID:</strong> {report_data['report_id']}</p>
            
            <h2>Executive Summary</h2>
            <p>{report_data['executive_summary']['overview']}</p>
            
            <h2>Key Metrics</h2>
            <div class="metric"><strong>Total Threats:</strong> {report_data['metrics']['total_threats']}</div>
            <div class="metric"><strong>Critical Threats:</strong> {report_data['metrics']['critical_threats']}</div>
            <div class="metric"><strong>Detection Accuracy:</strong> {report_data['metrics']['detection_accuracy']:.1%}</div>
            <div class="metric"><strong>Response Time:</strong> {report_data['metrics']['response_time']:.1f} minutes</div>
            
            <h2>Recommendations</h2>
            {''.join(f'<div class="recommendation">• {rec}</div>' for rec in report_data['recommendations'])}
            
            <footer>
                <p>Generated by NEXUS CYBER INTELLIGENCE Platform</p>
            </footer>
        </body>
        </html>
        """
        content = html_content
        size = len(html_content.encode())
        
    else:  # JSON format
        content = json.dumps(report_data, indent=2, default=str)
        size = len(content.encode())
    
    return {
        'success': True,
        'report': {
            'content': content,
            'format': format_type,
            'size': size
        },
        'metadata': report_data
    }

